5. الشروط والحلقات
*/

<?php
    // الشروط
    $number = 10;
    if ($number > 5) {
        echo "الرقم أكبر من 5";
    } else {
        echo "الرقم أصغر من أو يساوي 5";
    }
    
    // حلقة for
    for ($i = 0; $i < 5; $i++) {
        echo "الرقم هو: " . $i . "<br>";
    }
    
    // حلقة while
    $j = 0;
    while ($j < 3) {
        echo "العداد: " . $j . "<br>";
        $j++;
    }
?>